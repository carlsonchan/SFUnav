#import "studentServicesTableViewController.h"

@interface studentServicesTableViewController ()

@property (strong,nonatomic)NSArray *serviceOptions;
@property (strong,nonatomic)NSArray *service;

@end

@implementation studentServicesTableViewController
@synthesize serviceOptions, service;

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    NSString *conPath = [[NSBundle mainBundle]pathForResource:@"StudentServices" ofType:@"plist"];
    serviceOptions = [NSArray arrayWithContentsOfFile:conPath];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return serviceOptions.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"studentServicesCell" forIndexPath:indexPath];
    
    // Configure the cell...
    service = serviceOptions[indexPath.row];
    cell.textLabel.text = service;
    return cell;
}


